def fix_string(text: str, field_type: str = "content") -> str:
    """Apply all fixes to a JA string with CN residual."""
    if not text or not isinstance(text, str):
        return text

    original = text

    # 1. Fix character names
    for cn, ja in NAME_FIXES.items():
        text = text.replace(cn, ja)

    # 2. Fix satisfaction_type
    if field_type == "satisfaction_type":
        for cn, ja in SATISFACTION_FIXES.items():
            if text == cn:
                return ja
        # Generic: if contains CJK with no kana, it's likely untranslated
        if re.search(r'[\u4e00-\u9fff]', text) and not re.search(r'[\u3040-\u30ff]', text):
            # Try to transliterate: remove CN grammar, add の
            text = text.replace("爽快", "の爽快").replace("計略", "の計略")

    # 3. Fix emotion
    if field_type == "emotion":
        for cn, ja in EMOTION_FIXES.items():
            if text == cn:
                return ja

    # 4. Apply mixed pattern fixes (most specific first)
    for pattern, replacement in MIXED_PATTERNS:
        text = pattern.sub(replacement, text)

    # 5. Fix CN grammar words (longest match first to avoid partial replacements)
    sorted_grammar = sorted(GRAMMAR_FIXES.items(), key=lambda x: len(x[0]), reverse=True)
    for cn, ja in sorted_grammar:
        if cn in text:
            text = text.replace(cn, ja)

    return text


def fix_chapter(ch: dict) -> int:
    """Fix all residual strings in a chapter. Returns count of fixes."""
    fixes = 0

    def walk_and_fix(obj, field_type="content"):
        nonlocal fixes
        if isinstance(obj, dict):
            for k, v in obj.items():
                if k == "satisfaction_type" and isinstance(v, str):
                    fixed = fix_string(v, "satisfaction_type")
                    if fixed != v:
                        obj[k] = fixed
                        fixes += 1
                elif k == "emotion" and isinstance(v, str):
                    fixed = fix_string(v, "emotion")
                    if fixed != v:
                        obj[k] = fixed
                        fixes += 1
                elif isinstance(v, str):
                    # Fix ALL string fields (not just specific ones)
                    if k in ("title", "next_chapter_hook"):
                        ft = k
                    else:
                        ft = "content"
                    fixed = fix_string(v, ft)
                    if fixed != v:
                        obj[k] = fixed
                        fixes += 1
                elif isinstance(v, (dict, list)):
                    walk_and_fix(v)
        elif isinstance(obj, list):
            for item in obj:
                walk_and_fix(item)

    walk_and_fix(ch)
    return fixes


def main():
    import argparse
    ap = argparse.ArgumentParser()
    ap.add_argument("--chapters", type=str, default="all", help="Chapter range: 'all', '1-10', '6,7,8'")
    ap.add_argument("--dry-run", action="store_true", help="Don't write changes")
    args = ap.parse_args()

    if args.chapters == "all":
        ja_files = sorted(JA_DIR.glob("ch*.json"))
    elif "-" in args.chapters:
        start, end = args.chapters.split("-")
        ja_files = [JA_DIR / f"ch{int(i):04d}.json" for i in range(int(start), int(end) + 1)]
    else:
        nums = [int(x.strip()) for x in args.chapters.split(",")]
        ja_files = [JA_DIR / f"ch{n:04d}.json" for n in nums]

    total_fixes = 0
    total_chapters = 0
    for ja_path in ja_files:
        if not ja_path.exists():
            continue
        ch = json.loads(ja_path.read_text(encoding="utf-8"))
        fixes = fix_chapter(ch)
        if fixes > 0:
            total_fixes += fixes
            total_chapters += 1
            ch_num = ja_path.stem.replace("ch", "")
            print(f"ch{ch_num}: {fixes} fixes")
            if not args.dry_run:
                ja_path.write_text(json.dumps(ch, ensure_ascii=False, indent=2), encoding="utf-8")

    print(f"\nTotal: {total_fixes} fixes across {total_chapters} chapters")
    if args.dry_run:
        print("(dry run - no changes written)")


if __name__ == "__main__":
    main()
